// Recently used videos storage helper
export const getRecentlyUsedVideos = () => {
    const stored = localStorage.getItem('recentlyUsedVideos');
    return stored ? JSON.parse(stored) : [];
};

export const addRecentlyUsedVideo = (video) => {
    const recent = getRecentlyUsedVideos();
    const filtered = recent.filter(v => v.url !== video.url);
    const updated = [video, ...filtered].slice(0, 12);
    localStorage.setItem('recentlyUsedVideos', JSON.stringify(updated));
};

export const getRecommendedVideos = () => {
    // Return random videos from all categories
    const allVideos = videoCategories.flatMap(cat => cat.videos.map(v => ({ ...v, category: cat.title })));
    return allVideos.sort(() => Math.random() - 0.5).slice(0, 12);
};

export const getTrendingVideos = () => {
    // Return first few videos from each category as "trending"
    const allVideos = videoCategories.flatMap(cat => cat.videos.slice(0, 2).map(v => ({ ...v, category: cat.title })));
    return allVideos.slice(0, 12);
};

export const searchVideos = (query) => {
    const allVideos = videoCategories.flatMap(cat => cat.videos.map(v => ({ ...v, category: cat.title })));
    return allVideos.filter(v =>
        v.category.toLowerCase().includes(query.toLowerCase()) ||
        v.duration.includes(query)
    );
};

export const getVideosByCategory = (categoryTitle) => {
    const category = videoCategories.find(cat => cat.title === categoryTitle);
    return category ? category.videos : [];
};

// Custom videos by category storage
export const getCustomVideosByCategory = (categoryTitle) => {
    const stored = localStorage.getItem('customVideosByCategory');
    if (!stored) return [];
    const allCustomVideos = JSON.parse(stored);
    return allCustomVideos[categoryTitle] || [];
};

export const getAllCustomVideos = () => {
    const stored = localStorage.getItem('customVideosByCategory');
    return stored ? JSON.parse(stored) : {};
};

export const addCustomVideoToCategory = (categoryTitle, video) => {
    const stored = localStorage.getItem('customVideosByCategory');
    const allCustomVideos = stored ? JSON.parse(stored) : {};

    if (!allCustomVideos[categoryTitle]) {
        allCustomVideos[categoryTitle] = [];
    }

    // Add to beginning of array
    allCustomVideos[categoryTitle] = [video, ...allCustomVideos[categoryTitle]];

    localStorage.setItem('customVideosByCategory', JSON.stringify(allCustomVideos));
};

export const getVideosWithCustom = (categoryTitle) => {
    const category = videoCategories.find(cat => cat.title === categoryTitle);
    const defaultVideos = category ? category.videos : [];
    const customVideos = getCustomVideosByCategory(categoryTitle);

    // Return custom videos first, then default videos
    return [...customVideos, ...defaultVideos];
};
